function AdaptDescription($description, $numberOfCharcters) {
    $i = 0;
    $array = explode(".", trim($description));
    if (count($array) == 1 || strlen(trim($array[0]) . ". ") > $numberOfCharcters) {
        $array = explode(" ", trim($description));
        do {
            $adaptedDescription .= trim($array[$i]) . " ";
            $i++;
        } while ($i < count($array) && strlen($adaptedDescription . trim($array[$i]) . " ") < $numberOfCharcters);
        return $i == count($array) ? $adaptedDescription : $adaptedDescription . "...";
    } else {
        do {
            $adaptedDescription .= strlen($array[$i]) > 0 ? trim($array[$i]) . ". " : null;
            $i++;
        } while ($i < count($array) && strlen($adaptedDescription . trim($array[$i]) . ". ") < $numberOfCharcters);

        return $adaptedDescription;
    }
}

