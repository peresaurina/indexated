function CalculatePVPFromPriceWithoutTaxesAndDiscounts($basePrice, $taxType, $categoryIdentifier) {
    $descompte = 2;
    // Calculem el PVP
    if ($categoryIdentifier == 12) {
        $pvp = $basePrice * 1.35;
    } else {
        $pvp = ($taxType == "R" ? (float)$basePrice * 1.59 : (float) $basePrice * 1.8);
    }
    // Apliquem descompte
    $pvp = $pvp - $pvp * $descompte / 100;
    // Arrodonim
    return ceil($pvp * 20) / 20;
}

function CalculatePVPFromDBPrice($dbPrice, $taxType) {
    return round($taxType == "R" ? $dbPrice * 1.1 : $dbPrice * 1.21 , 2);
}

function CalculateDBPriceFromPVP($pvp, $taxType) {
    return $taxType == "R" ? $pvp / 1.1 : $pvp / 1.21;
}

function is_404($url) {
    $handle = curl_init($url);
    curl_setopt($handle,  CURLOPT_RETURNTRANSFER, TRUE);

    /* Get the HTML or whatever is linked in $url. */
    $response = curl_exec($handle);

    /* Check for 404 (file not found). */
    $httpCode = curl_getinfo($handle, CURLINFO_HTTP_CODE);
    curl_close($handle);

    /* If the document has loaded successfully without any redirection or error */
    if ($httpCode >= 200 && $httpCode < 300) {
        return false;
    } else {
        return true;
    }
}

function SimpleXML2Array($xml){
    $array = (array)$xml;

    //recursive Parser
    foreach ($array as $key => $value){
        if(strpos(get_class($value),"SimpleXML")!==false){
            $array[$key] = SimpleXML2Array($value);
        }
    }

    return $array;
}

